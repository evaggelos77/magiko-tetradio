function handleCommand(text){
  const t=(text||'').toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g,'').trim();
  if(!t) return;
  if(t.includes('επομεν') || t.includes('next')){
    if(state.screen==='story'){ storyNext(); } else { nextQuestion(false); speak(`${childFirstName()}, πάμε στην επόμενη ερώτηση.`); }
    return;
  }
  if(t.includes('βοηθ') || t.includes('help')){ const cq=currentQuestion(); state.feedback = cq.help || 'Πάμε μαζί βήμα-βήμα.'; state.screen='lesson'; speak(state.feedback); render(); return; }
  if(t.includes('παραμυθ')){ go('fairytales'); speak(`${childFirstName()}, πάμε στα παραμύθια.`); return; }
  if(t.includes('βιβλι') || t.includes('library')){ go('library'); speak(`${childFirstName()}, πάμε στην AI βιβλιοθήκη.`); return; }
  if(t.includes('διαβασ')){ runAction('readBook'); return; }
  if(t.includes('γον')){ go('parent'); return; }
  if(t.includes('μαθημ') || t.includes('ερωτη')){ go('lessonStart'); return; }
  state.feedback='Δεν κατάλαβα ακόμα. Δοκίμασε: επόμενη, βοήθεια, παραμύθι.'; render();
}
