function bookSentences(){
  const t=(state.bookText||'').replace(/\s+/g,' ').trim();
  const parts=t.match(/[^.!;;?]+[.!;;?]?/g) || [];
  return parts.map(s=>s.trim()).filter(s=>s.length>12).slice(0,18);
}
function bookWords(){
  const stop=new Set(['και','στο','στη','στην','στον','των','τον','την','ένα','μια','που','για','από','ότι','είναι','ήταν','του','της','τους','τις','με','σε','τα','το','οι','η','ο','να','δεν','κάθε','όταν']);
  const words=(state.bookText||'').toLowerCase().normalize('NFC').match(/[α-ωάέήίόύώϊϋΐΰa-z]{4,}/gi) || [];
  return [...new Set(words.map(w=>w.trim()).filter(w=>!stop.has(w)))].slice(0,80);
}
function bookSummary(){
  const s=bookSentences();
  if(!s.length) return 'Δεν υπάρχει ακόμα κείμενο. Ο γονέας ή ο δάσκαλος ανεβάζει ή επικολλά ένα παιδικό κείμενο.';
  return s.slice(0,3).join(' ');
}
function wrongSpell(word){
  const variants=[];
  if(word.includes('ι')) variants.push(word.replace('ι','η'));
  if(word.includes('η')) variants.push(word.replace('η','ι'));
  if(word.includes('ο')) variants.push(word.replace('ο','ω'));
  if(word.includes('ω')) variants.push(word.replace('ω','ο'));
  if(word.includes('αι')) variants.push(word.replace('αι','ε'));
  if(word.includes('ε')) variants.push(word.replace('ε','αι'));
  variants.push(word+'ς', word.slice(0,Math.max(2,word.length-1)));
  return [...new Set(variants.filter(v=>v && v!==word && v.length>2))].slice(0,3);
}
