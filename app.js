function startVoice(){
  const status=$('#voiceStatus');
  const SR=window.SpeechRecognition || window.webkitSpeechRecognition;
  if(!SR){ if(status) status.textContent='Ο browser δεν υποστηρίζει μικρόφωνο εδώ. Γράψε την εντολή στο πεδίο.'; speak('Γράψε την εντολή στο πεδίο.'); return; }
  const rec=new SR(); rec.lang='el-GR'; rec.interimResults=false; rec.maxAlternatives=1;
  if(status) status.textContent='Ακούω... πες: επόμενη, βοήθεια ή παραμύθι.';
  rec.onresult=(ev)=>{ const text=ev.results[0][0].transcript; if(status) status.textContent='Άκουσα: '+text; handleCommand(text); };
  rec.onerror=()=>{ if(status) status.textContent='Δεν άκουσα καθαρά. Δοκίμασε γραπτά.'; };
  rec.start();
}


